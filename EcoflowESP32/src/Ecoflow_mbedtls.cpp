#include "Ecoflow_mbedtls.h"
#include "mbedtls/bignum.h"
#include "mbedtls/ecp.h"

#ifndef MBEDTLS_MPI_CHK
#define MBEDTLS_MPI_CHK(f) do { if( ( ret = (f) ) != 0 ) goto cleanup; } while( 0 )
#endif

namespace Ecoflow_mbedtls {

// All values are big-endian
static const uint8_t secp160r1_p[] = { 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x7F, 0xFF, 0xFF, 0xFF };
static const uint8_t secp160r1_a[] = { 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x7F, 0xFF, 0xFF, 0xFC };
static const uint8_t secp160r1_b[] = { 0x1C, 0x97, 0xBE, 0xFC, 0x54, 0xBD, 0x7A, 0x8B, 0x65, 0xAC, 0xF8, 0x9F, 0x81, 0xD4, 0xD4, 0xAD, 0xC5, 0x65, 0xFA, 0x45 };
static const uint8_t secp160r1_gx[] = { 0x4A, 0x96, 0xB5, 0x68, 0x8E, 0xF5, 0x73, 0x28, 0x46, 0x64, 0x69, 0x89, 0x68, 0xC3, 0x8B, 0xB9, 0x13, 0xCB, 0xFC, 0x82 };
static const uint8_t secp160r1_gy[] = { 0x23, 0xA6, 0x28, 0x55, 0x31, 0x68, 0x94, 0x7D, 0x59, 0xDC, 0xC9, 0x12, 0x04, 0x23, 0x51, 0x37, 0x7A, 0xC5, 0xFB, 0x32 };
static const uint8_t secp160r1_n[] = { 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0xF4, 0xC8, 0xF9, 0x27, 0xAE, 0xD3, 0xCA, 0x75, 0x22, 0x57 };

int load_secp160r1_group(mbedtls_ecp_group *grp) {
    int ret;

    MBEDTLS_MPI_CHK(mbedtls_mpi_read_binary(&grp->P, secp160r1_p, sizeof(secp160r1_p)));
    MBEDTLS_MPI_CHK(mbedtls_mpi_read_binary(&grp->A, secp160r1_a, sizeof(secp160r1_a)));
    MBEDTLS_MPI_CHK(mbedtls_mpi_read_binary(&grp->B, secp160r1_b, sizeof(secp160r1_b)));
    MBEDTLS_MPI_CHK(mbedtls_mpi_read_binary(&grp->N, secp160r1_n, sizeof(secp160r1_n)));
    MBEDTLS_MPI_CHK(mbedtls_mpi_read_binary(&grp->G.X, secp160r1_gx, sizeof(secp160r1_gx)));
    MBEDTLS_MPI_CHK(mbedtls_mpi_read_binary(&grp->G.Y, secp160r1_gy, sizeof(secp160r1_gy)));
    MBEDTLS_MPI_CHK(mbedtls_mpi_lset(&grp->G.Z, 1));

    grp->pbits = 160;
    grp->nbits = 161;
    grp->id = (mbedtls_ecp_group_id)MBEDTLS_ECP_DP_SECP160R1;

cleanup:
    return ret;
}

}
